void filltable1(){
    DefaultTableModel model = (DefaultTableModel) tblHoaDonChiTiet.getModel();
    model.setRowCount(0);
    List<HoaDonChiTiet> list = hdctdao.selectAll();
    for(HoaDonChiTiet hdct:list){
        SanPham sp = spdao.selectById(hdct.getMaSP());
        Object[] row = {hdct.getMaHD(),hdct.getMaSP(),hdct.getSoLuong(),hdct.getMaBN(),sp.getDonGia()*hdct.getSoLuong()};
        model.addRow(row);
    }
}
