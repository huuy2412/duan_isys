void setForm1(HoaDonChiTiet hdct) {
    txtMaHD1.setText(hdct.getMaHD());
    txtSoLuong.setText(String.valueOf(hdct.getSoLuong()));
    
    // Đặt giá trị cho cboDatBan dựa trên hdct.getMaBN()
    for (int i = 0; i < cboDatBan.getItemCount(); i++) {
        Object item = cboDatBan.getItemAt(i);
        if (item instanceof BanNgoi) {
            BanNgoi bn = (BanNgoi) item;
            if (bn.getMaBN().equals(hdct.getMaBN())) {
                cboDatBan.setSelectedItem(bn);
                break;
            }
        }
    }
    
    // Đặt giá trị cho cboSanPham dựa trên hdct.getMaSP()
    for (int i = 0; i < cboSanPham.getItemCount(); i++) {
        Object item = cboSanPham.getItemAt(i);
        if (item instanceof SanPham) {
            SanPham sp = (SanPham) item;
            if (sp.getMaSP().equals(hdct.getMaSP())) {
                cboSanPham.setSelectedItem(sp);
                break;
            }
        }
    }
}


HoaDonChiTiet getForm1(){
    HoaDonChiTiet hdct = new HoaDonChiTiet();
    SanPham sp = (SanPham) cboSanPham.getSelectedItem();
    BanNgoi bn = (BanNgoi) cboDatBan.getSelectedItem();
    String mabn = bn.getMaBN();
    String masp = sp.getMaSP();
    
    hdct.setMaHD(txtMaHD1.getText());
    hdct.setMaBN(mabn);
    hdct.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
    hdct.setMaSP(masp);
    
    return hdct;
}
